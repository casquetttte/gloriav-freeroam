function WeaponUtility.GetNameByHash(hash)
	for id, weapon in pairs(Weapon) do
		if GetHashKey(id) == hash then
			return weapon.name
		end
	end

	return nil
end


function WeaponUtility.GetSpawningAmmo(weaponHash)
	local clipSize = GetWeaponClipSize(weaponHash)
	if clipSize <= 0 then clipSize = 1 end
	return clipSize * Settings.weaponClipCount
end
